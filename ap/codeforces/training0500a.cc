void function(istream& in, ostream& out) {
  ios::sync_with_stdio(false);
  in.tie(nullptr);

  int n, t;
  in >> n >> t;

  vector<int> cells;
  cells.resize(n + 1);

  for (int i=1; i<=n; i++) {
    in >> cells[i];
  }

  int pos = 1;
  while (pos < t) {
    pos += cells[pos];
  }

  if (pos == t) {
    out << "YES" << endl;
  }
  else {
    out << "NO" << endl;
  }
}

